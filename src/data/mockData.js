export function generateTicketCode() {
  const year = new Date().getFullYear()
  const random = Math.floor(100000 + Math.random() * 900000)
  return `LPR-${year}-${random}`
}

export function maskName(nama) {
  if (!nama || nama.length < 3) return '****'
  const parts = nama.split(' ')
  if (parts.length >= 2) {
    return `${parts[0]} ${parts[1].charAt(0)}****`
  }
  return `${nama.substring(0, 4)}****`
}

export function searchBansos(namaCari, desaId) {
  const data = bansosData[desaId] || []
  if (!namaCari) return []
  
  const searchLower = namaCari.toLowerCase()
  return data.filter(item => {
    // Simple fuzzy search - match if the visible part of masked name matches
    const visiblePart = item.nama_masked.split('*')[0].toLowerCase()
    return searchLower.startsWith(visiblePart.trim()) || 
           visiblePart.startsWith(searchLower.substring(0, visiblePart.length))
  })
}
